# Files the release ships but whose local version must survive an upgrade
# (these are user-local state, not framework assets).
PRESERVE_ON_UPGRADE = {"settings.local.json"}

# Framework version-metadata files that legitimately change on every upgrade.
# They are not user customizations, so excluded from the upgrade merge report
# (reporting them would be noise on every upgrade).
UPGRADE_META_FILES = {"VERSION", "CHANGELOG.md", "UPGRADE.md"}

# Patterns excluded from every ship copy. Dev-local / build artifacts that must
# never reach a target project. settings.local.json is also a PRESERVE_ON_UPGRADE
# file: on upgrade the user's version is carried forward from the backup, so this
# ignore only stops the release's own copy landing fresh — it never ships a dev
# permission whitelist into a new project. Keep in sync with SHIP_IGNORE in
# cairn_install.
SHIP_IGNORE = shutil.ignore_patterns(
    "__pycache__", "*.pyc", "node_modules", "settings.local.json",
)

# Framework-controlled, non-user-extensible top-level dirs under .claude/.
# A file absent from the new release inside one of these is a framework
# removal, NOT a user file, and must not be carried forward on upgrade —
# otherwise a removed artifact like runtime/readsets/cc-help.yaml resurrects
# and trips E_READSET009 / E_SCHEMA152. User-extensible framework dirs
# (hooks/, scripts/, skills/) are intentionally NOT listed, so user-added
# files there still survive; so do root-level user CC config (settings.json,
# agents/, commands/, mcp.json, ...).
FRAMEWORK_OWNED_DIRS = frozenset({
    "runtime", "schemas", "templates", "rules",
    "references", "docs", "evals", "fixtures", "workflows",
})


def _is_framework_owned(rel: str) -> bool:
    """True if ``rel`` lives under a framework-controlled, non-user-extensible dir."""
    top = rel.split("/", 1)[0]
    return top in FRAMEWORK_OWNED_DIRS


class UpgradeSafetyError(Exception):
    """Raised when an upgrade would unsafely clobber state."""


def _rel_file_set(root: Path) -> set[str]:
    """Posix-relative paths of every regular file under ``root``."""
    out: set[str] = set()
    if not root.exists():
        return out
    for p in root.rglob("*"):
        if p.is_file():
            out.add(p.relative_to(root).as_posix())
    return out


def _modified_framework_files(backup: Path, src: Path) -> list[str]:
    """User-customized framework files that the fresh swap overwrites.

    rel paths present in both ``backup`` (user's old version) and ``src`` (new
    release) whose bytes differ. Excludes ``PRESERVE_ON_UPGRADE`` (kept, not
    overwritten) and user-added files (absent from src, carried forward
    separately). Used for the upgrade merge report — does not alter overwrite
    semantics.
    """
    src_files = _rel_file_set(src)
    modified: list[str] = []
    for rel in sorted(_rel_file_set(backup)):
        if rel in PRESERVE_ON_UPGRADE or rel in UPGRADE_META_FILES or rel not in src_files:
            continue
        if (backup / rel).read_bytes() != (src / rel).read_bytes():
            modified.append(rel)
    return modified


def _replace_framework_dir(
    src: Path,
    dst: Path,
    *,
    label: str,
    force: bool = False,
) -> Path | None:
    """Safely replace ``dst`` with a fresh copy of ``src``.

    Used by init/update for both the project ``.claude/`` and the system
    install dir. Replaces the previous ``rmtree + copytree`` which silently
    destroyed any local modifications with no recovery path.

    Safety properties:
      * Identity check: if ``dst`` exists but has no ``VERSION`` file it is
        treated as a foreign directory and refused unless ``force=True``
        (prevents clobbering a non-Cairness ``.claude/``).
      * Backup: the previous ``dst`` is moved to ``<dst>.bak`` (one backup
        retained) so local changes are always recoverable.
      * Atomic-ish swap: the fresh tree is built in a temp dir beside ``dst``
        and renamed into place; on failure the backup is restored.
      * Preservation: files present in the old ``dst`` but absent from ``src``
        (user-added hooks/scripts) are copied back, and files in
        ``PRESERVE_ON_UPGRADE`` keep the user's local version. Files absent
        from ``src`` inside ``FRAMEWORK_OWNED_DIRS`` are framework removals,
        not user files — they are dropped (recoverable in the backup) so a
        removed artifact like ``runtime/readsets/cc-help.yaml`` cannot
        resurrect and trip ``E_READSET009`` / ``E_SCHEMA152``.

    Returns the backup ``Path`` if one was made, else ``None``.
    """
    parent = dst.parent
    parent.mkdir(parents=True, exist_ok=True)

    if dst.name == ".cairness":
        raise UpgradeSafetyError(
            f"Refusing to replace {dst}: '.cairness/' is project state, "
            f"not a replaceable framework root."
        )

    had_existing = dst.exists()
    backup: Path | None = None

    if had_existing:
        if not force and not (dst / "VERSION").is_file():
            raise UpgradeSafetyError(
                f"{dst} exists but is not a Cairness install (no VERSION file). "
                f"Refusing to overwrite a foreign directory. Re-run with --force "
                f"to override."
            )
        backup = dst.with_name(dst.name + ".bak")
        if backup.exists():
            shutil.rmtree(backup)
        os.rename(dst, backup)

    # Build the fresh tree in a temp dir beside dst, then rename into place.
    tmp = Path(tempfile.mkdtemp(prefix=dst.name + ".tmp-", dir=str(parent)))
    try:
        # tmp is an empty dir created by mkdtemp; copy into it.
        shutil.copytree(src, tmp, dirs_exist_ok=True, ignore=SHIP_IGNORE)
        os.rename(tmp, dst)
    except Exception:
        if tmp.exists():
            shutil.rmtree(tmp, ignore_errors=True)
        if backup is not None and backup.exists() and not dst.exists():
            os.rename(backup, dst)
        raise

    # Carry forward user-added files and protected local files from the backup.
    # Files absent from the new source inside FRAMEWORK_OWNED_DIRS are framework
    # removals, not user files — drop them instead of resurrecting stale
    # artifacts (e.g. a removed runtime/readsets/cc-help.yaml). Dropped files
    # stay recoverable in the backup.
    if backup is not None and backup.exists():
        src_files = _rel_file_set(src)
        dropped: list[str] = []
        for rel in sorted(_rel_file_set(backup)):
            if rel in PRESERVE_ON_UPGRADE or rel not in src_files:
                if rel not in src_files and _is_framework_owned(rel):
                    dropped.append(rel)
                    continue
                src_file = backup / rel
                target = dst / rel
                target.parent.mkdir(parents=True, exist_ok=True)
                shutil.copy2(src_file, target)
        if dropped:
            print(f"  {len(dropped)} stale framework file(s) removed by this upgrade "
                  f"(retained in {backup.name}/):")
            for rel in dropped:
                print(f"    - {rel}")

    # Merge report: surface user-customized framework files that the fresh
    # swap overwrote. Report-only — overwrite semantics are unchanged; the
    # user's versions remain in the backup for re-application.
    if backup is not None and backup.exists():
        modified = _modified_framework_files(backup, src)
        if modified:
            print(f"  {len(modified)} framework file(s) you customized were overwritten by the new version:")
            for rel in modified:
                print(f"    - {rel}")
            print(f"  Your versions are retained in {backup.name}/ ; re-apply customizations or diff against the new files.")
            report_path = dst.parent / f"{dst.name}.merge-report.json"
            report_path.write_text(
                json.dumps({"overwritten_modified_files": modified}, ensure_ascii=False, indent=2) + "\n",
                encoding="utf-8",
            )
            print(f"  Merge report: {report_path}")

    if backup is not None:
        print(f"  Previous {label} backed up to: {backup}")
    return backup


def _adapter_installation_plan(data_dir: Path, project_root: Path, adapter: str):
    """Load and resolve one declared adapter before making project changes."""
    manifest = data_dir / ADAPTER_INSTALLATION_DIR / f"{adapter}.yaml"
    installation = load_adapter_installation(
        manifest, data_dir / ADAPTER_INSTALLATION_SCHEMA
    )
    plan = build_adapter_installation_plan(
        installation, core_root=data_dir, project_root=project_root
    )
    if plan.adapter != adapter:
        raise AdapterInstallationError(
            f"adapter installation identity {plan.adapter!r} does not match "
            f"requested adapter {adapter!r}"
        )
    unsupported = [op.name for op in plan.operations if op.action == "generate"]
    if unsupported:
        raise ValueError(
            "adapter installation generate operations are not supported: "
            + ", ".join(unsupported)
        )
    return plan


def _framework_root_from_prefix(project_root: Path, framework_prefix: str) -> Path:
    """Resolve persisted layout metadata without allowing project escape."""
    root = Path(project_root).resolve()
    layout = RuntimeLayout(
        project_root=root,
        core_root=root / framework_prefix,
        state_root=root / ".cairness",
        framework_prefix=framework_prefix,
    )
    framework_root = layout.core_root
    try:
        framework_root.relative_to(root)
    except ValueError as exc:
        raise ValueError(
            f"framework prefix escapes the project root: {framework_prefix!r}"
        ) from exc
    return framework_root


def _restore_failed_adapter_swap(
    framework_root: Path, backup: Path | None
) -> None:
    """Roll back a completed core swap when a host-asset operation fails."""
    if framework_root.exists():
        shutil.rmtree(framework_root)
    if backup is not None and backup.exists():
        os.rename(backup, framework_root)


def _file_snapshot(path: Path) -> bytes | None:
    """Capture an optional small state file for post-swap rollback."""
    return path.read_bytes() if path.is_file() else None


def _restore_file_snapshot(path: Path, snapshot: bytes | None) -> None:
    """Restore the exact pre-update state of an optional state file."""
    if snapshot is None:
        path.unlink(missing_ok=True)
        return
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_bytes(snapshot)


def _file_state_snapshot(path: Path) -> tuple[bytes, int] | None:
    """Capture file content and permissions for rollback of executable hooks."""
    if not path.is_file():
        return None
    return path.read_bytes(), path.stat().st_mode


def _restore_file_state_snapshot(
    path: Path, snapshot: tuple[bytes, int] | None
) -> None:
    if snapshot is None:
        path.unlink(missing_ok=True)
        return
    content, mode = snapshot
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_bytes(content)
    path.chmod(mode)


def _directory_file_snapshots(path: Path) -> dict[str, tuple[bytes, int]]:
    if not path.is_dir():
        return {}
    return {
        item.name: snapshot
        for item in path.iterdir()
        if item.is_file() and (snapshot := _file_state_snapshot(item)) is not None
    }


def _restore_directory_file_snapshots(
    path: Path, snapshots: dict[str, tuple[bytes, int]], *, existed: bool
) -> None:
    if path.is_dir():
        for item in path.iterdir():
            if item.is_file() and item.name not in snapshots:
                item.unlink()
    for name, snapshot in snapshots.items():
        _restore_file_state_snapshot(path / name, snapshot)
    if not existed and path.is_dir():
        try:
            path.rmdir()
        except OSError:
            pass


def _existing_directories(root: Path) -> set[Path]:
    if not root.is_dir():
        return set()
    return {root, *(path for path in root.rglob("*") if path.is_dir())}


def _remove_new_empty_directories(root: Path, existing: set[Path]) -> None:
    if not root.exists():
        return
    directories = [path for path in root.rglob("*") if path.is_dir()]
    directories.append(root)
    for path in sorted(directories, key=lambda item: len(item.parts), reverse=True):
        if path not in existing:
            try:
                path.rmdir()
            except OSError:
                pass


def _require_custom_layout_identity(
    framework_root: Path, *, adapter: str, contract_prefix: str, selected_prefix: str
) -> None:
    """Reject metadata redirects that do not identify an installed adapter."""
    if selected_prefix == contract_prefix:
        return
    manifest = framework_root / ADAPTER_INSTALLATION_DIR / f"{adapter}.yaml"
    schema = framework_root / ADAPTER_INSTALLATION_SCHEMA
    try:
        installation = load_adapter_installation(manifest, schema)
    except (OSError, ValueError) as exc:
        raise ValueError(
            f"custom framework layout {selected_prefix!r} has no valid "
            f"adapter installation identity for {adapter!r}: {manifest}"
        ) from exc
    if (
        installation.adapter != adapter
        or installation.framework_prefix != selected_prefix
    ):
        raise ValueError(
            f"custom framework layout {selected_prefix!r} adapter installation "
            f"identity does not match {adapter!r}: {manifest}"
        )


def _publish_install_result(
    project_root: Path,
    *,
    adapter: str,
    framework_prefix: str,
    report: dict,
) -> None:
    """Publish install metadata and report as one rollback-safe record pair."""
    metadata_path = project_root / INSTALL_METADATA_RELATIVE
    report_path = project_root / UPGRADE_REPORT_RELATIVE
    metadata_snapshot = _file_snapshot(metadata_path)
    report_snapshot = _file_snapshot(report_path)
    try:
        _write_install_identity(
            project_root,
            adapter=adapter,
            framework_prefix=framework_prefix,
        )
        _write_upgrade_report(project_root, report)
    except Exception:
        _restore_file_snapshot(metadata_path, metadata_snapshot)
        _restore_file_snapshot(report_path, report_snapshot)
        raise


def _execute_adapter_operations(plan, framework_root: Path) -> list[dict[str, str]]:
    """Apply validated copy operations to an already-installed core tree."""
    applied: list[dict[str, str]] = []
    for operation in plan.operations:
        relative = operation.target.relative_to(plan.framework_root)
        target = framework_root / relative
        if operation.action == "copy-file":
            target.parent.mkdir(parents=True, exist_ok=True)
            shutil.copy2(operation.source, target)
        elif operation.action == "copy-tree":
            shutil.copytree(
                operation.source,
                target,
                dirs_exist_ok=True,
                ignore=SHIP_IGNORE,
            )
        else:
            raise ValueError(
                f"unsupported adapter installation action: {operation.action}"
            )
        applied.append(
            {
                "name": operation.name,
                "action": operation.action,
                "source": operation.source.relative_to(plan.core_root).as_posix(),
                "target": relative.as_posix(),
            }
        )
    return applied


def _upgrade_file_changes(src: Path, dst: Path) -> dict[str, list[str]]:
    """Describe upgrade effects before the framework swap mutates ``dst``."""
    if not dst.exists():
        return {
            "overwritten_modified_files": [],
            "dropped_stale_files": [],
            "preserved_files": [],
        }
    src_files = _rel_file_set(src)
    old_files = _rel_file_set(dst)
    return {
        "overwritten_modified_files": _modified_framework_files(dst, src),
        "dropped_stale_files": sorted(
            rel for rel in old_files - src_files if _is_framework_owned(rel)
        ),
        "preserved_files": sorted(
            rel
            for rel in old_files
            if rel in PRESERVE_ON_UPGRADE
            or (rel not in src_files and not _is_framework_owned(rel))
        ),
    }


def _write_upgrade_report(project_root: Path, report: dict) -> Path:
    """Atomically publish a completed installation or upgrade report."""
    path = project_root / UPGRADE_REPORT_RELATIVE
    path.parent.mkdir(parents=True, exist_ok=True)
    fd, temporary = tempfile.mkstemp(
        prefix=".upgrade-report.", suffix=".tmp", dir=path.parent
    )
    try:
        with os.fdopen(fd, "w", encoding="utf-8") as stream:
            json.dump(report, stream, ensure_ascii=False, indent=2)
            stream.write("\n")
            stream.flush()
            os.fsync(stream.fileno())
        os.replace(temporary, path)
    except Exception:
        try:
            os.unlink(temporary)
        except OSError:
            pass
        raise
    return path


