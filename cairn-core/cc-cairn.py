def find_repo():
    d = Path.cwd()
    while d != d.parent:
        if (d / "cairn-core" / "VERSION").exists() and (d / "cairn_install").exists():
            return d
        d = d.parent
    return None


def pull_repo(repo):
    result = subprocess.run(
        ["git", "pull", "origin", "main"],
        cwd=repo, capture_output=True, text=True
    )
    if result.returncode != 0:
        sys.exit(f"git pull failed: {result.stderr}")
    return result.stdout.strip()


def _repo_remote_url(repo):
    """Return origin URL for repo, or '' if unavailable / not a git repo."""
    result = subprocess.run(
        ["git", "remote", "get-url", "origin"],
        cwd=repo, capture_output=True, text=True
    )
    if result.returncode != 0:
        return ""
    return result.stdout.strip()


def ensure_repo():
    repo = find_repo()
    if repo is not None:
        remote = _repo_remote_url(repo)
        if remote and remote != REMOTE_URL:
            # Found a local checkout (e.g. a fork) whose origin is not the
            # official upstream. Don't surprise-pull; use it as-is so the user
            # controls updates to their own checkout.
            print(f"Using local Cairness checkout at {repo}")
            print(f"  (origin={remote} != {REMOTE_URL}; skipping auto-pull. "
                  f"Update the checkout manually if needed.)")
            return repo
        return repo

    clone_path = Path.home() / ".local" / "share" / "cairness-repo"
    if not clone_path.exists():
        print(f"Cloning Cairness from {REMOTE_URL} ...")
        result = subprocess.run(
            ["git", "clone", REMOTE_URL, str(clone_path)],
            capture_output=True, text=True
        )
        if result.returncode != 0:
            sys.exit(f"git clone failed: {result.stderr}")
    return clone_path


def sync_system_install(data_dir, repo):
    """Update system installation from repo. Returns True if updated."""
    print("Pulling latest from remote ...")
    pull_repo(repo)

    new_commit = git_head_commit(repo)
    old_commit = read_commit_hash(data_dir)

    if old_commit == new_commit:
        version = "unknown"
        vf = repo / "cairn-core" / "VERSION"
        if vf.exists():
            version = vf.read_text().strip()
        print(f"System installation already up to date (v{version}, {new_commit[:7]}).")
        return data_dir  # no update needed

    core_src = repo / "cairn-core"
    new_ver = (core_src / "VERSION").read_text().strip()
    old_ver = "none"
    vf = data_dir / "VERSION"
    if vf.exists():
        old_ver = vf.read_text().strip()

    print(f"Updating system installation: v{old_ver} → v{new_ver}")
    _replace_framework_dir(core_src, data_dir, label="system install")
    write_commit_hash(data_dir, new_commit)
    print(f"System installation updated to v{new_ver} ({new_commit[:7]}).")
    return data_dir


def _ensure_baseline_gitignored(project_root):
    """Ensure transient per-machine verification baselines stay out of git.

    Two effects, both idempotent:
      1. Append the baseline/ gitignore rule if missing (covers new installs and
         projects whose .gitignore predates the rule).
      2. `git rm --cached` any already-tracked baseline files (role-baseline.json,
         pre-apply.json, ...) so the next commit removes them from version control.
         gitignore only blocks untracked files; tracked ones must be unstaged.

    Baselines are per-machine worktree snapshots, not team-shared truth —
    committing them causes cross-machine noise and false conflicts. Safe to run
    repeatedly; silently no-ops when there is nothing to do or git is absent.
    """
    gitignore = project_root / ".gitignore"
    existing = gitignore.read_text() if gitignore.exists() else ""
    if BASELINE_GITIGNORE_RULE not in existing:
        # Ensure the rule sits under a Cairness header if we wrote one; append
        # is sufficient since gitignore rules are order-independent.
        if gitignore.exists():
            with gitignore.open("a") as f:
                if not existing.endswith("\n"):
                    f.write("\n")
                f.write(f"{BASELINE_GITIGNORE_RULE}\n")
        else:
            gitignore.write_text(f"{BASELINE_GITIGNORE_RULE}\n", encoding="utf-8")
        print(f"  Updated .gitignore ({BASELINE_GITIGNORE_RULE})")

    # Not every project is a git repo (e.g. init before `git init`); skip cleanup
    # there. `git rev-parse --git-dir` is the standard repo probe.
    probe = subprocess.run(
        ["git", "rev-parse", "--git-dir"],
        cwd=str(project_root), capture_output=True, text=True,
    )
    if probe.returncode != 0:
        return

    # List tracked files under any change's baseline/ dir, relative to repo root.
    # Pathspec globs are unreliable across git versions when invoked without a
    # shell (subprocess passes `*` literally and git's pathspec matching of the
    # `baseline/` segment is inconsistent), so list all tracked files under the
    # changes tree and filter in Python — paths are repo-root-relative already.
    ls = subprocess.run(
        ["git", "ls-files", "--", ".cairness/changes/"],
        cwd=str(project_root), capture_output=True, text=True,
    )
    if ls.returncode != 0:
        return
    tracked = [
        line.strip() for line in ls.stdout.splitlines()
        if line.strip() and f"/baseline/" in f"{line.strip()}/"
    ]
    if not tracked:
        return
    rm = subprocess.run(
        ["git", "rm", "--cached", "--quiet", "--", *tracked],
        cwd=str(project_root), capture_output=True, text=True,
    )
    if rm.returncode == 0:
        print(f"  Untracked {len(tracked)} committed baseline file(s) "
              f"(kept on disk; will be dropped on next commit):")
        for path in tracked:
            print(f"    {path}")
    else:
        # Don't fail the install/update over a git index issue; surface it.
        print(f"  warning: could not untrack committed baseline files: {rm.stderr.strip()}",
              file=sys.stderr)


def _install_git_hooks(
    project_root: Path, framework_root: Path | None = None
) -> None:
    """Install Cairness git hooks from .claude/hooks/ into .git/hooks/.

    Only the hooks listed in the hook_map are installed; framework hooks
    (like no-spec-no-code.py) that are Claude Code PreToolUse hooks, not
    git hooks, stay in .claude/hooks/ only.
    """
    hooks_src = framework_root or (project_root / ".claude")
    hooks_src = hooks_src / "hooks"
    git_hooks_dir = project_root / ".git" / "hooks"

    # Not a git repo — skip (same guard as _ensure_baseline_gitignored).
    probe = subprocess.run(
        ["git", "rev-parse", "--git-dir"],
        cwd=str(project_root), capture_output=True, text=True,
    )
    if probe.returncode != 0:
        return

    # Map: framework hook filename (under .claude/hooks/) → git hook name.
    hook_map = {"pre-commit": "pre-commit"}

    for fw_name, git_name in hook_map.items():
        src = hooks_src / fw_name
        if not src.is_file():
            continue
        dst = git_hooks_dir / git_name

        if dst.exists():
            try:
                content = dst.read_text(encoding="utf-8", errors="replace")
            except OSError:
                content = ""
            if "Cairness" in content:
                # Existing Cairness-managed hook — update in place.
                shutil.copy2(src, dst)
                dst.chmod(0o755)
            else:
                # Non-Cairness hook exists — back up, warn, install.
                backup = dst.with_suffix(".bak")
                shutil.move(str(dst), str(backup))
                shutil.copy2(src, dst)
                dst.chmod(0o755)
                print(f"  ⚠ Existing .git/hooks/{git_name} backed up to "
                      f".git/hooks/{git_name}.bak")
        else:
            shutil.copy2(src, dst)
            dst.chmod(0o755)


def sync_project(data_dir, project_root):
    """Update the metadata-selected project adapter from the system install."""
    project_root = Path(project_root).resolve()
    metadata = read_install_metadata(project_root, strict=True)
    legacy = not metadata and (project_root / ".claude" / "VERSION").is_file()
    adapter = metadata.get("adapter") or ("claude-code" if legacy else "")
    if not adapter:
        version = (data_dir / "VERSION").read_text().strip()
        print(f"System installation is v{version}.")
        print("Run 'cc-cairn init' in a project directory to install the framework.")
        return False

    plan = _adapter_installation_plan(data_dir, project_root, adapter)
    framework_prefix = metadata.get("framework_prefix") or plan.framework_prefix
    # Validate persisted layout metadata before using it as a filesystem path.
    framework_dir = _framework_root_from_prefix(project_root, framework_prefix)
    if not framework_dir.exists():
        version = (data_dir / "VERSION").read_text().strip()
        print(f"System installation is v{version}.")
        print(
            f"Configured framework layout {framework_prefix}/ is missing; "
            "run 'cc-cairn init' to install it."
        )
        return False
    _require_custom_layout_identity(
        framework_dir,
        adapter=adapter,
        contract_prefix=plan.framework_prefix,
        selected_prefix=framework_prefix,
    )

    new_commit = read_commit_hash(data_dir)
    old_commit = read_commit_hash(framework_dir)

    new_ver = (data_dir / "VERSION").read_text().strip()
    version_file = framework_dir / "VERSION"
    project_ver = (
        version_file.read_text(encoding="utf-8").strip()
        if version_file.is_file()
        else "unknown"
    )

    if new_commit and old_commit and new_commit == old_commit:
        print(f"Project already up to date (v{project_ver}, {old_commit[:7]}).")
        _publish_install_result(
            project_root,
            adapter=plan.adapter,
            framework_prefix=framework_prefix,
            report={
                "version": 1,
                "status": "passed",
                "adapter": plan.adapter,
                "source_layout": framework_prefix,
                "target_layout": framework_prefix,
                "from_version": project_ver,
                "to_version": new_ver,
                "backup": "",
                "legacy_migration": legacy,
                "operations": [],
                "overwritten_modified_files": [],
                "dropped_stale_files": [],
                "preserved_files": [],
            },
        )
        return False

    changes = _upgrade_file_changes(data_dir, framework_dir)
    print(f"Updating project {framework_prefix}/: v{project_ver} → v{new_ver}")
    metadata_path = project_root / ".cairness" / "install.yaml"
    report_path = project_root / UPGRADE_REPORT_RELATIVE
    metadata_snapshot = _file_snapshot(metadata_path)
    report_snapshot = _file_snapshot(report_path)
    hook_path = project_root / ".git" / "hooks" / "pre-commit"
    hook_backup_path = hook_path.with_suffix(".bak")
    hook_snapshot = _file_state_snapshot(hook_path)
    hook_backup_snapshot = _file_state_snapshot(hook_backup_path)
    backup = None
    swapped = False
    try:
        backup = _replace_framework_dir(
            data_dir,
            framework_dir,
            label=f"project {framework_prefix}/",
        )
        swapped = True
    except UpgradeSafetyError as exc:
        print(f"error: {exc}", file=sys.stderr)
        print("hint: if this is intentionally a non-Cairness directory, run "
              "'cc-cairn init' with overwrite, or remove it manually.", file=sys.stderr)
        sys.exit(1)
    try:
        operations = _execute_adapter_operations(plan, framework_dir)
        # Re-install / update git hooks.
        _install_git_hooks(project_root, framework_dir)
        _publish_install_result(
            project_root,
            adapter=plan.adapter,
            framework_prefix=framework_prefix,
            report={
                "version": 1,
                "status": "passed",
                "adapter": plan.adapter,
                "source_layout": framework_prefix,
                "target_layout": framework_prefix,
                "from_version": project_ver,
                "to_version": new_ver,
                "backup": str(backup) if backup else "",
                "legacy_migration": legacy,
                "operations": operations,
                **changes,
            },
        )
    except Exception:
        try:
            if swapped:
                _restore_failed_adapter_swap(framework_dir, backup)
        finally:
            _restore_file_snapshot(metadata_path, metadata_snapshot)
            _restore_file_snapshot(report_path, report_snapshot)
            _restore_file_state_snapshot(hook_path, hook_snapshot)
            _restore_file_state_snapshot(hook_backup_path, hook_backup_snapshot)
        raise
    # Backfill the transient-baseline ignore rule after the installation
    # transaction has committed; this cleanup is not part of update identity.
    _ensure_baseline_gitignored(project_root)
    print(f"Project updated to v{new_ver}. Local additions were preserved; "
          f"previous {framework_prefix}/ backed up to {framework_dir.name}.bak.")
    return True


